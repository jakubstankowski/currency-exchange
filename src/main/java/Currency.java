class Currency {
    private double basicPrice;
    private double finallSellPrice;
    private double finallBuyPrice;


    double getBasicPrice() {
        return basicPrice;
    }

    void setBasicPrice(double basicPrice) {
        this.basicPrice = basicPrice;
    }

    double getFinallSellPrice() {
        return finallSellPrice;
    }

    void setFinallSellPrice(double finallSellPrice) {
        this.finallSellPrice = finallSellPrice;
    }

    double getFinallBuyPrice() {
        return finallBuyPrice;
    }

    void setFinallBuyPrice(double finallBuyPrice) {
        this.finallBuyPrice = finallBuyPrice;
    }

}
